"""
Скрипт для создания аналитической части ВКР в формате Word
"""

from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def setup_document_formatting(doc):
    """Настройка форматирования документа согласно ГОСТу"""
    sections = doc.sections
    for section in sections:
        section.left_margin = Cm(3)
        section.right_margin = Cm(1)
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
    
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Times New Roman'
    font.size = Pt(14)
    paragraph_format = style.paragraph_format
    paragraph_format.line_spacing_rule = WD_LINE_SPACING.EXACTLY
    paragraph_format.line_spacing = Pt(21)
    paragraph_format.first_line_indent = Cm(1.25)
    paragraph_format.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    paragraph_format.space_before = Pt(0)
    paragraph_format.space_after = Pt(0)

def add_chapter_title(doc, text):
    """Заголовок главы"""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text.upper())
    run.font.name = 'Times New Roman'
    run.font.size = Pt(16)
    run.bold = True
    p.paragraph_format.space_after = Pt(12)
    return p

def add_section_title(doc, text):
    """Заголовок параграфа"""
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(1.25)
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(14)
    run.bold = True
    p.paragraph_format.space_after = Pt(6)
    return p

def add_paragraph(doc, text):
    """Обычный абзац"""
    p = doc.add_paragraph(text)
    p.paragraph_format.first_line_indent = Cm(1.25)
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    return p

def add_table_for_screenshot(doc, title):
    """Таблица для скриншота"""
    table = doc.add_table(rows=2, cols=1)
    table.style = 'Light Grid Accent 1'
    
    cell = table.rows[0].cells[0]
    cell.text = title
    cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
    cell.paragraphs[0].runs[0].bold = True
    
    cell = table.rows[1].cells[0]
    cell.text = '[ЗДЕСЬ ВСТАВИТЬ СКРИНШОТ]'
    cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
    cell.paragraphs[0].runs[0].italic = True
    
    return table

