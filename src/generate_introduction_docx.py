        header = section.header
        paragraph = header.paragraphs[0]
        paragraph.text = f"–ê—Ö—É–Ω–æ–≤ –î.–†. ¬´–ü–ª–∞—Ç—Ñ–æ—Ä–º–∞ —Ç—É—Ä–æ–≤ –ø–æ –¢–∞—Ç–∞—Ä—Å—Ç–∞–Ω—É¬ª"
