    def add_numbered_list(self, items: list):
        """–ù—É–º–µ—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫"""
        for i, item in enumerate(items, 1):
            para = self.document.add_paragraph()
